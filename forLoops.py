"""
For Loop Syntax Structure

for [iterating variable] in [sequence]:
    [do something]

"""

# Loop through a range function
for i in range(6):
   print(i)


# Iterate throught a number of values
# range(start, stop)
for i in range(0,5):
   print(i)


# You also do this
# range(start, stop, step)
for i in range(0,15,3):
   print(i)
